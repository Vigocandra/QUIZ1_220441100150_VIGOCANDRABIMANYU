    private void cjenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cjenisActionPerformed
        pilihan = (String) cjenis.getSelectedItem();
        switch (pilihan) {
            case "Arabica Coffe":
                harga_kopi = 25000;
                break;
            case "Robusta Coffe":
                harga_kopi = 50000;
                break;
            case "Liberian Coffe":
                harga_kopi = 60000;
                break;
            case "Excelsa Coffe":
                harga_kopi = 75000;
                break;
           
        }
        txtharga.setText(""+Integer.toString(harga_kopi));

        
    }//GEN-LAST:event_cjenisActionPerformed

    private void bselesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bselesaiActionPerformed
        
        item = Integer.parseInt(txtjumlah.getText());
        int TotalHarga = harga_kopi * item;

        txttotal.setText("Rp."+TotalHarga);
        txtitem.setText(""+item);
        txtkopi.setText(""+pilihan);
                 
    }//GEN-LAST:event_bselesaiActionPerformed

